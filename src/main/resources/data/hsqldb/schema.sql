DROP TABLE BillRegister if EXISTS;

create table BillRegister(
billNum BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 100, INCREMENT BY 1) NOT NULL,
senderAcNo VARCHAR(10) NOT NULL,
receiverAcNo VARCHAR(10) NOT NULL,
amount INTEGER NOT NULL,
billStatus BOOLEAN,
billMessage VARCHAR(1000),
PRIMARY KEY (billNum)
);